/*

 * 1.3
 
 
 * Describe the steps that transform a program written in a
 * high-level language such as C into a representation that is
 * directly executed by a computer processor.
 
 
 * Answer
 
 
 * Step 1
 * Compiler transforms the C code (high level) into Assembly code.
 
 * Step 2
 * Assembler transforms the Assembly code (low level) into Machine code.
 
 * Step 3
 * Machine code (lowest level) is executed by the hardware.
 
 */